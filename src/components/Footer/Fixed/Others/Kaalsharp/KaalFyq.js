const KaalFYQ = () => {
  const questions = [
    {
      id: 1,
      title: "Q-1:- Why do people perform Kaal Sarp Dosha Pooja?",
      content: `This Dosha in Vedic astrology occurs when all the 7 planets lie between Rahu and Ketu. Thus, any native facing this issue seems to have an adverse effect on their horoscope. So, to abolish and wear off the ill effects of the Kaal Sarp Dosha, people must perform the Kaal Sarp Dosha Pooja.`,
    },
    {
      id: 2,
      title: "Q-2:- What is Kaal Sarp Shanti?",
      content: `Kaal Sarp Nivaran or Kaal Sarp Shanti is the method astrologers perform to wear off the ill impacts of the Kaal Sarp Dosha in the horoscope. Astrologers suggest remedies and Pooja that shall help people be away from the ill effects of the Kaal Sarp Dosha.`,
    },
    {
      id: 3,
      title:
        "Q-3:- When is the best time to perform the Kaal Sarp Dosha Pooja?",
      content: `Performing the Pooja on the Amavasya is considered to be highly auspicious. However, people can also perform the Kaal Sarp Dosha Pooja on an eclipse day. Also, Sundays, Tuesdays, and Naga Panchmi would be great days too to perform the Pooja.`,
    },
    {
      id: 4,
      title: "Q-4:- How to know if I have Kaal Sarp Dosha?",
      content: `As per astrology, there are 12 types of Kaal Sarp Dosha. When a person possesses a Kaal Sarp Dosha in his/her Kundli, they shall dream about snakes often in their dreams.`,
    },
  ];
  const Question = ({ question }) => {
    const initiallyOpen = question.id === 1 || question.id === 2;
    const [open, setOpen] = useState(initiallyOpen);

    const contentWithLineBreaks = question.content
      ? question.content.split("\n").map((line, index) => (
          <React.Fragment key={index}>
            {line}
            <br />
          </React.Fragment>
        ))
      : null;

    return (
      <AccordionSection
        className={`accordion ${open ? "open" : "closed"}`}
        id={`accordion-${question.id}`}
        onClick={() => setOpen(!open)}
      >
        <AccordionHeader
          className={open ? "open" : "closed"}
          id="accordion-two"
        >
          <QuestionTitle>{question.title}</QuestionTitle>
          <AccordionButton onClick={() => setOpen(!open)} className="btns">
            {open ? <CaretUp /> : <CaretDown />}
          </AccordionButton>
        </AccordionHeader>
        {open && <AccordionPara>{contentWithLineBreaks}</AccordionPara>}
      </AccordionSection>
    );
  };
  return (
    <>
      <ContainerAccordion>
        <h1>Frequently Asked Questions</h1>
        <div className="questions">
          {questions.map((question) => (
            <Question key={question.id} question={question} />
          ))}
        </div>
      </ContainerAccordion>
    </>
  );
};

