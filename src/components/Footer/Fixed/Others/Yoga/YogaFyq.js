      content: `Vidyut means shining, lightning, or glittering. Thus, this Yoga generally bestows auspicious impacts on the life of the natives and lets them shine in terms of gains and profits. This Yoga lets the people stand out amongst the crowd. A Vidyut Yoga in astrology forms when the lord of the 11 house is exalted and conjoins with planet Venus and the two fall at an angle from the ascendant lord.`,
