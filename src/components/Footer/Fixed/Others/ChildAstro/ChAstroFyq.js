const ChAstroFYQ = () => {
  const questions = [
    {
      id: 1,
      title: "Q-1:- Which house in the horoscope tells about the child?",
      content: `The 5th house in the horoscope reveals the children. Thus, by analysing the fifth house, an individual can understand the situations and scenarios that shall form to have a child in life. This house also reveals the details about how the relationship of the child would be with his/her parents and the planets, aspects, and signs that would be responsible for the same.`,
    },
    {
      id: 2,
      title: "Q-2:- How can astrology predict the birth of a child?",
      content: `As per the Vedic astrology, the strength of the 2nd, 5th, and 10th houses reveals it all. Analysing these houses along with the planets Sun and Jupiter (for female child) and Mercury (for a male child) must be done.`,
    },
    {
      id: 3,
      title: "Q-3:- How can we find out about the third child in astrology?",
      content: `As per astrology, the house of the younger sibling is the third house. Therefore, one can predict the 2nd child from the seventh house (third from the 5th house). And with that, the third child can be seen by analysing the ninth house in the horoscope, i.e., the third house from the seventh house.`,
    },
    {
      id: 4,
      title: "Q-4:- Which Nakshatras are auspicious for childbirth?",
      content: `In Vedic astrology, the 4 most favourable Nakshatras for the birth of the child are Ashwini, Bharani, Pushya, and Magha Nakshatra.`,
    },
  ];
  const Question = ({ question }) => {
    const initiallyOpen = question.id === 1 || question.id === 2;
    const [open, setOpen] = useState(initiallyOpen);

    const contentWithLineBreaks = question.content
      ? question.content.split("\n").map((line, index) => (
          <React.Fragment key={index}>
            {line}
            <br />
          </React.Fragment>
        ))
      : null;

    return (
      <AccordionSection
        className={`accordion ${open ? "open" : "closed"}`}
        id={`accordion-${question.id}`}
        onClick={() => setOpen(!open)}
      >
        <AccordionHeader
          className={open ? "open" : "closed"}
          id="accordion-two"
        >
          <QuestionTitle>{question.title}</QuestionTitle>
          <AccordionButton onClick={() => setOpen(!open)} className="btns">
            {open ? <CaretUp /> : <CaretDown />}
          </AccordionButton>
        </AccordionHeader>
        {open && <AccordionPara>{contentWithLineBreaks}</AccordionPara>}
      </AccordionSection>
    );
  };
  return (
    <>
      <ContainerAccordion>
        <h1>Frequently Asked Questions</h1>
        <div className="questions">
          {questions.map((question) => (
            <Question key={question.id} question={question} />
          ))}
        </div>
      </ContainerAccordion>
    </>
  );
};

export default ChAstroFYQ;
